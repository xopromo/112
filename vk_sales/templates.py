def classify_intent(text: str) -> str:
    """Определяет намерение из текста пользователя."""
    lower = text.lower()
    for intent, keywords in INTENT_PATTERNS.items():
        if any(kw in lower for kw in keywords):
            return intent
    return "UNKNOWN"
