def render(template_key: str, variant: str, name: str = "", city: str = "") -> str:
    """Подставляет переменные в шаблон."""
    variants = TEMPLATES.get(template_key, {})
    text = variants.get(variant) or variants.get("A", "")
    return text.format(
        name=name or "друг",
        city=city or "вашем городе"
    )


